package t20230416_복습;
// 대입 연산자
public class T10_AssignmentOperatorExample {

	public static void main(String[] args) {
		int result = 0;
		result += 10;	//result=10
		System.out.println("result=" + result);
		result -= 5;	//result=5
		System.out.println("result=" + result);
		result *= 3;	//result=15
		System.out.println("result=" + result);
		result /= 5;	//result=3
		System.out.println("result=" + result);
		result %= 3;	//result=0
		System.out.println("result=" + result);
	}

}
